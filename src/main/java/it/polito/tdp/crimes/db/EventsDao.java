	public List<District> getVertici(Integer anno) {
		String sql = "SELECT e.district_id AS id, AVG(e.geo_lon) AS longit, AVG(e.geo_lat) AS latitud "
				+ "FROM `events` AS e "
				+ "WHERE year(e.reported_date) = ? "
				+ "GROUP BY e.district_id "
				+ "ORDER BY e.district_id";
		
		try {
			Connection conn = DBConnect.getConnection() ;

			PreparedStatement st = conn.prepareStatement(sql) ;
			st.setInt(1, anno);
			
			List<District> list = new ArrayList<>() ;
			
			ResultSet res = st.executeQuery() ;
			
			while(res.next()) {
				try {
					list.add(new District(res.getInt("id"), res.getDouble("longit"), res.getDouble("latitud")));
				} catch (Throwable t) {
					t.printStackTrace();
					System.out.println(res.getInt("id"));
				}
			}
			
			conn.close();
			return list ;

		} catch (SQLException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
			return null ;
		}
	}

